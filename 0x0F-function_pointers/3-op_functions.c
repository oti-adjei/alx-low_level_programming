#include "3-calc.h"

/**
 * op_add - adds it parameters
 *
 * @a: first argument
 * @b:second argument
 *
 * Return: returns the sum
 * of a and b
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtracts it
 * second argument from first
 *
 * @a: first argument
 * @b:second argument
 *
 * Return:  returns the difference
 * of a and b
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplies it parameters
 *
 * @a: first argument
 * @b:second argument
 *
 * Return: returns the product
 * of a and b
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides it first argument
 * by second argument
 *
 * @a: first argument
 * @b:second argument
 *
 * Return: returns the result of the division
 * of a by b
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulo of first argument
 * by  second
 *
 * @a: first argument
 * @b:second argument
 *
 * Return: he remainder of the division
 * of a by b
 */

int op_mod(int a, int b)
{
return (a % b);
}
